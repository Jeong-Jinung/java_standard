package ch02;

public class Ex2_1 {

    public static void main(String[] args) {

        //print() - 출력후에 줄바꿈을 안함
        //println() - 출력 후에 줄바꿈을 합니다.
        System.out.println(5+3); // 덧셈
        System.out.println(5-3); // 뺄셈
        System.out.println(5*3); // 곱셈
        System.out.println(5/3); // 나눗셈
        System.out.println(5/3.0); // 나눗셈
    }
}
